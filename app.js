function updateScrollIndicator(track, rail, thumb) {
  if (!track || !rail || !thumb) return;
  const scrollable = Math.max(0, track.scrollWidth - track.clientWidth);
  const progress = scrollable ? Math.min(1, Math.max(0, track.scrollLeft / scrollable)) : 0;
  const railWidth = rail.clientWidth;
  const visibleRatio = track.scrollWidth ? Math.min(1, track.clientWidth / track.scrollWidth) : 1;
  const thumbWidth = Math.min(railWidth, Math.max(48, railWidth * visibleRatio));

  thumb.style.width = `${thumbWidth}px`;
  thumb.style.transform = `translateX(${progress * Math.max(0, railWidth - thumbWidth)}px)`;

  const shell = track.closest(".route-scroll-shell, .history-scroll-shell");
  shell?.classList.toggle("is-at-start", track.scrollLeft <= 2);
  shell?.classList.toggle("is-at-end", track.scrollLeft >= scrollable - 2);
}

function updateRouteState() {
  if (!routeTrack || !routeSlides.length) return;
  const activeIndex = Math.min(
    routeSlides.length - 1,
    Math.max(0, Math.round(routeTrack.scrollLeft / Math.max(1, routeTrack.clientWidth))),
  );

  routeSlides.forEach((slide, index) => {
    const active = index === activeIndex;
    slide.classList.toggle("is-active", active);
    slide.setAttribute("aria-hidden", String(!active));
  });
  updateScrollIndicator(routeTrack, routeRail, routeThumb);
}

function updateHistoryState() {
  updateScrollIndicator(historyTrack, historyRail, historyProgress);
  if (!historyTrack) return;
  const scrollable = Math.max(0, historyTrack.scrollWidth - historyTrack.clientWidth);
  if (scrollable <= 2 || historyTrack.scrollLeft >= scrollable - 2) historyAutoplayComplete = true;

  if (historyCards.length) {
    const trackLeft = historyTrack.getBoundingClientRect().left;
    let activeCard = historyCards[0];
    let activeDistance = Number.POSITIVE_INFINITY;
    historyCards.forEach((card) => {
      const distance = Math.abs(card.getBoundingClientRect().left - trackLeft);
      if (distance < activeDistance) {
        activeDistance = distance;
        activeCard = card;
      }
    });
    historyCards.forEach((card) => {
      const active = card === activeCard;
      card.classList.toggle("is-active", active);
      if (active) card.setAttribute("aria-current", "step");
      else card.removeAttribute("aria-current");
    });
  }
}

function enableSwipeNavigation(track, options = {}) {
  if (!track) return;
  let activePointer = null;
  let startX = 0;
  let startScrollLeft = 0;
  let lastDistance = 0;

  const finishSwipe = (event) => {
    if (activePointer === null || event.pointerId !== activePointer) return;

    const finishedPointer = activePointer;
    activePointer = null;
    if (track.hasPointerCapture(finishedPointer)) track.releasePointerCapture(finishedPointer);
    track.classList.remove("is-dragging");

    if (options.snap && track.clientWidth) {
      const startIndex = Math.round(startScrollLeft / track.clientWidth);
      const direction = lastDistance < 0 ? 1 : -1;
      const targetIndex = Math.abs(lastDistance) > 42
        ? startIndex + direction
        : Math.round(track.scrollLeft / track.clientWidth);
      const maxIndex = Math.max(0, track.children.length - 1);
      track.scrollTo({
        left: Math.min(maxIndex, Math.max(0, targetIndex)) * track.clientWidth,
        behavior: reducedMotion.matches ? "auto" : "smooth",
      });
    }

    options.onEnd?.();
  };

  track.addEventListener("pointerdown", (event) => {
    if (!event.isPrimary || (event.pointerType === "mouse" && event.button !== 0)) return;
    if (event.pointerType === "touch") return;
    activePointer = event.pointerId;
    startX = event.clientX;
    startScrollLeft = track.scrollLeft;
    lastDistance = 0;
    track.setPointerCapture(activePointer);
    track.classList.add("is-dragging");
    options.onStart?.();
  });

  track.addEventListener("pointermove", (event) => {
    if (event.pointerId !== activePointer) return;
    lastDistance = event.clientX - startX;
    if (Math.abs(lastDistance) > 3) event.preventDefault();
    track.scrollLeft = startScrollLeft - lastDistance;
  }, { passive: false });

  track.addEventListener("pointerup", finishSwipe);
  track.addEventListener("pointercancel", finishSwipe);
  track.addEventListener("lostpointercapture", (event) => {
    if (event.pointerId !== activePointer) return;
    track.classList.remove("is-dragging");
    activePointer = null;
    options.onEnd?.();
  });
  track.addEventListener("dragstart", (event) => event.preventDefault());
  track.addEventListener("touchstart", () => options.onStart?.(), { passive: true });
  track.addEventListener("touchend", () => options.onEnd?.(), { passive: true });
  track.addEventListener("touchcancel", () => options.onEnd?.(), { passive: true });

  track.addEventListener("keydown", (event) => {
    if (!["ArrowLeft", "ArrowRight", "PageUp", "PageDown"].includes(event.key)) return;
    event.preventDefault();
    options.onStart?.();
    const direction = event.key === "ArrowLeft" || event.key === "PageUp" ? -1 : 1;
    track.scrollBy({ left: direction * track.clientWidth * 0.86, behavior: "smooth" });
    options.onEnd?.();
  });
}

